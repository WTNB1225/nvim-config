local opt = vim.opt

-- display line numbers
opt.number = true

-- indent
opt.tabstop = 4
opt.shiftwidth = 4
opt.expandtab = true

-- search
opt.ignorecase = true
opt.smartcase = true
opt.incsearch = true
opt.hlsearch = true

--encoding
opt.encoding = "utf-8"
opt.fileencoding = "utf-8"

-- title
opt.title = true

opt.splitright = true